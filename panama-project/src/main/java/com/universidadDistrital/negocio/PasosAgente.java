package com.universidadDistrital.negocio;

import java.sql.Date;

public class PasosAgente {
	Date f_fecha;
	int k_num_serie;
	String n_sentido;
	float v_costo;
	int k_idAgente;
	int k_id;
	
	public Date getF_fecha() {
		return f_fecha;
	}
	public void setF_fecha(Date f_fecha) {
		this.f_fecha = f_fecha;
	}
	public int getK_num_serie() {
		return k_num_serie;
	}
	public void setK_num_serie(int k_num_serie) {
		this.k_num_serie = k_num_serie;
	}
	public String getN_sentido() {
		return n_sentido;
	}
	public void setN_sentido(String n_sentido) {
		this.n_sentido = n_sentido;
	}
	public float getV_costo() {
		return v_costo;
	}
	public void setV_costo(float v_costo) {
		this.v_costo = v_costo;
	}
	public int getK_idAgente() {
		return k_idAgente;
	}
	public void setK_idAgente(int k_idAgente) {
		this.k_idAgente = k_idAgente;
	}
	public int getK_id() {
		return k_id;
	}
	public void setK_id(int k_id) {
		this.k_id = k_id;
	}
	
	
	
}
