package com.universidadDistrital.negocio;

public class Agente {
	int k_id;
	int k_nit;
	int k_idPersona;
	String n_user;
	public int getK_id() {
		return k_id;
	}
	public void setK_id(int k_id) {
		this.k_id = k_id;
	}
	public int getK_nit() {
		return k_nit;
	}
	public void setK_nit(int k_nit) {
		this.k_nit = k_nit;
	}
	public int getK_idPersona() {
		return k_idPersona;
	}
	public void setK_idPersona(int k_idPersona) {
		this.k_idPersona = k_idPersona;
	}
	public String getN_user() {
		return n_user;
	}
	public void setN_user(String n_user) {
		this.n_user = n_user;
	}
	
	
}
