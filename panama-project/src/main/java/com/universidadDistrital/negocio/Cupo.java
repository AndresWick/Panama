package com.universidadDistrital.negocio;

public class Cupo {

	private String kcupo ;
	private String icupobuque ;
	/*yyyy-[m]m-[d]d*/
	private String fcupo;
	private String itipoperiodo;
	private String isentido;
	private String iestado;
	
	public String getKcupo() {
		return kcupo;
	}
	public void setKcupo(String kcupo) {
		this.kcupo = kcupo;
	}
	public String getIcupobuque() {
		return icupobuque;
	}
	public void setIcupobuque(String icupobuque) {
		this.icupobuque = icupobuque;
	}
	public String getFcupo() {
		return fcupo;
	}
	public void setFcupo(String fcupo) {
		this.fcupo = fcupo;
	}
	public String getItipoperiodo() {
		return itipoperiodo;
	}
	public void setItipoperiodo(String itipoperiodo) {
		this.itipoperiodo = itipoperiodo;
	}
	public String getIsentido() {
		return isentido;
	}
	public void setIsentido(String isentido) {
		this.isentido = isentido;
	}
	public String getIestado() {
		return iestado;
	}
	public void setIestado(String iestado) {
		this.iestado = iestado;
	}
	
	
}
